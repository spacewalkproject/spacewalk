sub add_static_ar {
    $PERL_STATIC_AR ||= "";
    my $cur = $APACHE_SRC =~ /^../ ? "$PWD/" : "";
    for (qw(blib/arch/auto arch/auto)) {
	last if -d ($ar_dir = "$APACHE_SRC/modules/perl/$_");
